#include "libft.h"
#include <stdlib.h>

void	ft_lstdel(t_list **alst, void (*del)(void*, size_t))
{
	t_list	*l;
	t_list	*nl;

	l = *alst;
	while (l)
	{
		nl = l->next;
		del(l->content, l->content_size);
		free(l);
		l = nl;
	}
	*alst = NULL;
}
