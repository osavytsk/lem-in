#include "libft.h"
#include <stdlib.h>

void		*ft_memalloc(size_t size)
{
	char	*p;
	size_t	i;

	i = 0;
	if ((p = malloc(size)) == NULL)
		return (NULL);
	while (i < size)
		p[i++] = 0;
	return (p);
}
