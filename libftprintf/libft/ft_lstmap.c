#include "libft.h"
#include <stdlib.h>

t_list		*ft_lstmap(t_list *lst, t_list *(*f)(t_list *elem))
{
	t_list	*result;

	if (lst == NULL || f == NULL)
		return (NULL);
	if ((result = malloc(sizeof(t_list))) == NULL)
		return (NULL);
	result = f(lst);
	result->next = ft_lstmap(lst->next, f);
	return (result);
}
