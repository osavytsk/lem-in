#include "libft.h"
#include <stdlib.h>

char		*ft_strdup(const char *str)
{
	size_t	i;
	char	*dup;

	i = ft_strlen(str);
	if ((dup = malloc(sizeof(*str) * (i + 1))) == NULL)
		return (NULL);
	dup = ft_strcpy(dup, str);
	return (dup);
}
